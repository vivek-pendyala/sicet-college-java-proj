package org.example.module1_intro.operators;

public class UnaryOpr {
    public static void main( String[] args){
        int a=10;
        int b=20;
        a++;
        ++a;
        b--;
        --b;
        System.out.println(a);
        System.out.println(b);
        System.out.println(b--);
        System.out.println(a++);
    }
}
